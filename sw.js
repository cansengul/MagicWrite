// MagicWrite – Service Worker
// Ermöglicht Offline-Start und Installation als PWA (iOS/Android).
// Bump CACHE_VERSION whenever index.html or a cached asset changes,
// so returning users get the new version instead of a stale cache.
const CACHE_VERSION = 'magicwrite-v1';
const APP_SHELL = [
  './',
  './index.html',
  './manifest.json',
  './icons/icon-192.png',
  './icons/icon-512.png',
  './icons/apple-touch-icon.png',
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_VERSION).then((cache) => cache.addAll(APP_SHELL))
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(
        keys.filter((key) => key !== CACHE_VERSION).map((key) => caches.delete(key))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  const { request } = event;

  // Only handle simple same-origin GET requests. Everything else
  // (Firebase auth/database, Google Fonts, lucide icons, POST/PUT, etc.)
  // goes straight to the network so live data and login keep working.
  if (request.method !== 'GET' || new URL(request.url).origin !== self.location.origin) {
    return;
  }

  event.respondWith(
    caches.match(request).then((cached) => {
      const network = fetch(request)
        .then((response) => {
          if (response && response.ok) {
            const clone = response.clone();
            caches.open(CACHE_VERSION).then((cache) => cache.put(request, clone));
          }
          return response;
        })
        .catch(() => cached);
      // Serve cached shell instantly if available, refresh cache in background.
      return cached || network;
    })
  );
});
